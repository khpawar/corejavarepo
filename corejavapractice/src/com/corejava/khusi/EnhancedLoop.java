package com.corejava.khusi;
  
public class EnhancedLoop{
   public static void main(String[] args){
	   
	    int [] numbers = {10,20,30,40,50};
	     
	    for(int x : numbers){
	    	System.out.print(x);
	    	System.out.print(",");
	    
	    }
	    
	    String [] name= {"Riya","Seema"};
	    for(String y : name){
	    	System.out.print(y);
	    	System.out.print(",");
	    	
	    }
   }
}
