package com.corejava.khusi;

public class IfElseStatement {
	public static void main(String args[]){
		int x = 30;

		if (x < 20){
			System.out.println(x);
			System.out.print("This is if statement");
		}else {
			System.out.println(x);
			System.out.println("This is else statement");
		}
	}
}
